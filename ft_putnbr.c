#include "libft.h"

void	ft_putnbr(int n)
{
	char			c;
	unsigned int	m;

	if (n < 0)
	{
		ft_putchar('-');
		m = -n;
	}
	else
		m = n;
	if (m < 10)
	{
		c = m + 48;
		ft_putchar(c);
	}
	if (m > 9)
	{
		ft_putnbr((m - m % 10) / 10);
		ft_putnbr(m % 10);
	}
}
