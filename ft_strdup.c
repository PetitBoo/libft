#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t			len;
	unsigned int	i;
	char			*s2;

	len = ft_strlen(s1);
	i = 0;
	s2 = (char *)malloc(sizeof(*s2) * len + 1);
	if (s2 == NULL)
		return (NULL);
	while (i < len)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
