#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*lst;
	t_list	*tmp;

	lst = *alst;
	while (lst)
	{
		tmp = lst;
		(*del)(lst->content, lst->content_size);
		free(lst);
		lst = tmp->next;
	}
	*alst = NULL;
}
