#include "libft.h"

int		ft_free(char **str)
{
	int	i;

	i = 0;
	if (str && *str)
	{
		while (*str)
		{
			free(str[i]);
			i++;
		}
		free(str);
		str = NULL;
	}
	return (0);
}
